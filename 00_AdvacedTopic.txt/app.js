// const variable

// Constant Arrays
// --> you can change the elements of constant array.

// Example:

//You can create a constant array:
const cars = ["Thar", "Indigo", "BMW"];
console.log(cars);
//You can change an element:
cars[0] = "Saab";
console.log(cars);

//You can add an element:
cars.push("Audi");
console.log(cars);
